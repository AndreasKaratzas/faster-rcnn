def list2tup(L: List[int]) -> Tuple[Tuple[int]]:
    return tuple([(l,) for l in tuple(L)])


def flatten(data):
    if isinstance(data, tuple):
        for x in data:
            yield from flatten(x)
    else:
        yield data
